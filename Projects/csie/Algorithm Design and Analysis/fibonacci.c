long long *times(long long F1[], const long long S[]) {
	long long *F2 = (long long *) malloc(3 * sizeof(long long));
	F2[0] = (F1[0] * S[0] + F1[1] * S[1]) % 1000000007;
	F2[1] = (F1[0] * S[1] + F1[1] * S[2]) % 1000000007;
	F2[2] = (F1[1] * S[1] + F1[2] * S[2]) % 1000000007;
	free(F1);
	return F2;
}

int main(int argc, char *argv[]) {
	int T, a, b, N;
	long long *F;
	scanf("%d\n", &T);
	for (int t = 0; t < T; t++) {
		scanf("%d %d %d\n", &a, &b, &N);
		N--;
		F = (long long *) malloc(3 * sizeof(long long));
		F[0] = 1; F[1] = 0; F[2] = 1;
		int n = ceil(log2(N + 1));
		for (int i = 0; i < n; i++)
			if ((1 << i) & N)
				F = times(F, TABLE[i]);
		free(F);
		printf("%lld\n", (F[1] * b + F[2] * a) % 1000000007);
	}
	return 0;
}
